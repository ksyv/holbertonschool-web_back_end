#!/usr/bin/env python3
"""
Modul for task 2:
Write a type-annotated function to_str that takes
a float n as arguments
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """return the string representation of a float

    Args:
        n (float): float argument

    Returns:
        str: string representation of argument
    """
    return str(n)
